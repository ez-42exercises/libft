char	*strchr(const char *s, int c)
{
	int		i;
	char	*temp;

	i = 0;
	temp = (char *)s;
	while (temp[i] != '\0')
	{
		if (temp[i] == c)
			return ((char *)(temp + i));
		i++;
	}
	return (0);
}
