void	*memmove(void *dest, const void *src, size_t n)
{
	int	i;

	if (dest > src)
	{
		i = n - 1;
		while (i >= 0)
		{
			*(dest + i) = *(src + i);
			i--;
		}
	}
	else
	{
		i = 0;
		while (i < n)
		{
			*(dest + i) = *(src + i);
			i++;
		}
	}
	return (dest);
}
