#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned long int	i;

	if (dest > src)
	{
		i = n;
		while (i > 0)
		{
			i--;
			*(char *)(dest + i) = *(char *)(src + i);
		}
	}
	else
	{
		i = 0;
		while (i < n)
		{
			*(char *)(dest + i) = *(char *)(src + i);
			i++;
		}
	}
	return (dest);
}
